          Sports is another area where color blindness may inhibit some from playing at their full potential. Take cricket for instance. Because cricket is played with a dark pink ball on green grass, playing cricket with color blindness (also spelled "colour blindness") can easily cause confusion and dangerous in-play situations during this fast-moving sport.
          <br>
          <br>
          Unfortunately, those who are color blind also miss out on one of the most colorful experiences of the day: the sunset. The vibrant pinks, yellows, purples, reds, and oranges of the setting sun are often mostly lost on the color blind. The lower light conditions of sunset mixed with the presence of red in most of its colors make a sunset look bland and somewhat homogenous in color across the sky. Those who are color blind are unable to see the deeply varied layers of color in between.

